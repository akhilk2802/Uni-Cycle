const mongoose = require("mongoose");

const productSchema = mongoose.Schema({
  imgsrc: {
    type: String,
    required: true,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  quantity: {
    type: Number,
    required: true,
  } 
});

const Product = mongoose.model("product", productSchema);

module.exports = Product;
